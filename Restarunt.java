
package com.vedait.json.conversion;

public class Restarunt {

	private String name;
	private String address;
	private String pincode;
	private String RestaruntType;
	private String minCost;
	private String logo;
	private String serviceType;

	public Restarunt() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPincode() {
		return pincode;
	}

	public void setPincode(String pincode) {
		this.pincode = pincode;
	}

	public String getRestaruntType() {
		return RestaruntType;
	}

	public void setRestaruntType(String RestaruntType) {
		this.RestaruntType = RestaruntType;
	}

	public String getMinCost() {
		return minCost;
	}

	public void setMinCost(String minCost) {
		this.minCost = minCost;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getServiceType() {
		return serviceType;
	}

	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}

}
